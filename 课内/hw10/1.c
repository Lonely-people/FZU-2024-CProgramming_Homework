void ShowDayWeek(int dow) {
    if (dow == 0) {
        printf("日");
    } else if (dow == 1) {
        printf("一");
    } else if (dow == 2) {
        printf("二");
    } else if (dow == 3) {
        printf("三");
    } else if (dow == 4) {
        printf("四");
    } else if (dow == 5) {
        printf("五");
    } else if (dow == 6) {
        printf("六");
    }
}