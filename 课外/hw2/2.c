#include <stdio.h>

int main() {
	double n; scanf("%lf", &n);
	double ans = 0;
	if (n <= 36000) {
		ans = n * 0.03;
	} else if (n > 36000 && n <= 144000) {
		ans = 36000 * 0.03 + (n - 36000) * 0.10;
	} else if (n > 144000 && n <= 300000) {
		ans = 36000 * 0.03 + (144000 - 36000) * 0.10 + (n - 144000) * 0.20;
	} else if (n > 300000 && n <= 420000) {
		ans = 36000 * 0.03 + (144000 - 36000) * 0.10 + (300000 - 144000) * 0.20 + (n - 300000) * 0.25;
	} else if (n > 420000 && n <= 660000) {
		ans = 36000 * 0.03 + (144000 - 36000) * 0.10 + (300000 - 144000) * 0.20 + (420000 - 300000) * 0.25 + 
			(n - 420000) * 0.30;
	} else if (n > 660000 && n <= 960000) {
		ans = 36000 * 0.03 + (144000 - 36000) * 0.10 + (300000 - 144000) * 0.20 + (420000 - 300000) * 0.25 + 
			(660000 - 420000) * 0.30 + (n - 660000) * 0.35;
	} else if (n > 9600000) {
		ans = 36000 * 0.03 + (144000 - 36000) * 0.10 + (300000 - 144000) * 0.20 + (420000 - 300000) * 0.25 + 
			(660000 - 420000) * 0.30 + (960000 - 660000) * 0.35 + (n - 960000) * 0.45;
	}
	printf("%.2lf\n", ans);
	return 0;
}